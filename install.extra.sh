#!/usr/bin/bash

(( EUID != 0 )) && { echo >&2 "This script should be run as root!"; exit 1; }

# -------------------------------------------------------------------------------------------------------------------- #
# CONFIGURATION.
# -------------------------------------------------------------------------------------------------------------------- #

ver="11"
url="https://download.opensuse.org/repositories"
name="pkgstore.deb"
repos=(
  'ext'
  'fix'
)

# -------------------------------------------------------------------------------------------------------------------- #
# INITIALIZATION.
# -------------------------------------------------------------------------------------------------------------------- #

init() {
  # Run.
  install_extra
}

# -------------------------------------------------------------------------------------------------------------------- #
# EXTRA.
# -------------------------------------------------------------------------------------------------------------------- #

install_extra() {
  for i in "${repos[@]}"; do
    echo "deb [signed-by=/etc/apt/trusted.gpg.d/${name}-${i}.gpg] ${url}/home:/pkgstore:/deb:/${i}/Debian_${ver}/ /" | tee "/etc/apt/sources.list.d/${name}-${i}.list" \
      && curl -fsSL "${url}/home:pkgstore:deb:${i}/Debian_${ver}/Release.key" | gpg --dearmor | tee "/etc/apt/trusted.gpg.d/${name}-${i}.gpg" > /dev/null
  done
  apt update;
}

# -------------------------------------------------------------------------------------------------------------------- #
# -------------------------------------------------< INIT FUNCTIONS >------------------------------------------------- #
# -------------------------------------------------------------------------------------------------------------------- #

init "$@"; exit 0
