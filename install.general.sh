#!/usr/bin/bash

(( EUID != 0 )) && { echo >&2 "This script should be run as root!"; exit 1; }

# -------------------------------------------------------------------------------------------------------------------- #
# CONFIGURATION.
# -------------------------------------------------------------------------------------------------------------------- #

ver="11"
url="https://download.opensuse.org/repositories"
name="pkgstore.deb"

# -------------------------------------------------------------------------------------------------------------------- #
# INITIALIZATION.
# -------------------------------------------------------------------------------------------------------------------- #

init() {
  # Run.
  install_general
}

# -------------------------------------------------------------------------------------------------------------------- #
# GENERAL.
# -------------------------------------------------------------------------------------------------------------------- #

install_general() {
  echo "deb [signed-by=/etc/apt/trusted.gpg.d/${name}.gpg] ${url}/home:/pkgstore:/deb/Debian_${ver}/ /" | tee "/etc/apt/sources.list.d/${name}.list" \
    && curl -fsSL "${url}/home:pkgstore:deb/Debian_${ver}/Release.key" | gpg --dearmor | tee "/etc/apt/trusted.gpg.d/${name}.gpg" > /dev/null \
    && apt update
}

# -------------------------------------------------------------------------------------------------------------------- #
# -------------------------------------------------< INIT FUNCTIONS >------------------------------------------------- #
# -------------------------------------------------------------------------------------------------------------------- #

init "$@"; exit 0
